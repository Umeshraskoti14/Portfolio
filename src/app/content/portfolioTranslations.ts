export function getLocalizedPortfolioItem<T extends BasePortfolioItem>(language: LanguageMode, item: T): T {
  if (language === 'en') return item;

  const translation = portfolioNepaliCopy[item.title];
  if (!translation) return item;

  return {
    ...item,
    title: translation.title,
    description: translation.description,
    tags: translation.tags ?? item.tags,
  };
}
